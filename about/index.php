            <p>東狼研究所の原点を語る上で欠かせないものがあります。<br>それは「リアル脱出ゲームTV」という番組と、「Twitter」という媒体です。</p>
            <p>2013年1月1日に第一回が放送され、2014年6月26日に放送終了した「リアル脱出ゲームTV」警察とテロリスト「謎男」との攻防を描き、リアルタイムでWebサイトから視聴者が謎解きに参加できるという衝撃的な非日常を演出し、多くの注目を集めた番組。<br>インターネット媒体を用いた非日常の演出を志す我々の団体の姿勢は、この番組からの影響を強くおけており、「東狼研究所」という組織名は、「リアル脱出ゲームTV」に登場したテロ組織「イーストウルフ」をイメージしています。</p>
            <p>最初に我々が皆様に「謎解き」を仕掛けたのは「Twitter」からでした。<br>「謎解き」というものが広く認知され展開されていく昨今、自らが作った謎を解いてほしい、あるいは誰かが作った謎を解きたいという人は、インターネット上にも数多く存在しています。</p>
            <p>東狼研究所ではそんな「一匹狼」から提供された謎を形にし、インターネットを使いリアルより多くの人に出題し、また、インターネットならではのギミックを使い、時には様々なページ、動画、音声、画像やストーリーなどを用意し、皆様を非日常の世界へと招待する、「世界一平和なテロ組織」です。</p>
